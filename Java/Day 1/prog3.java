public class prog3 {

    public static void main(String[] args) {

        // Escape Sequence

        /*
         * Hello
         * World
         * 
         */
        System.out.println("Hello \nWorld");

        /*
         * 
         * \n - New Line
         * \t - Tab
         */
        System.out.println("Hello \tWorld");

        // My Name Is "Sagnik"

        System.out.println("My Name Is Sagnik");
        System.out.println("My Name Is \"Sagnik\"");

    }

}
