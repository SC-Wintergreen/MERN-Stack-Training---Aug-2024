// For Loop

public class prog1 {

    public static void main(String[] args) {

        // For Loop

        // i++ - i=i+1; i+=1

        for (int i = 0; i <= 5; i++)
            System.out.println("I live in India");

        for (int i = 5; i >= 0; i--)
            System.out.println("I live in India");

    }
}